function xmdengji(fen) {

    switch (parseInt(fen / 10)) {
        case 10: return "优";
        case 9: return "优";
        case 8: return "良";
        case 7: return "中";
        case 6: return "中";
        default: return "差";
    }


};


//立项评级提交表单按钮内容
function lxpj_smtFm() {

    url = '/xiangmuguanli/lxpj_edit?name='+xmname+'&xmid='+xmid;

    //  $.messager.progress();	 提示进度条
    $('#lxpj_fm').form('submit', {
        url: url,
        success: function (result) {
            //var result = eval('(' + result + ')');
            result = JSON.parse(result);   //IE浏览器在后台操作完成后返回提示信息，转化为json字符串格式

            if (result.success == true) {
                $.messager.progress('close');	// 关闭进度条
                $.messager.show({
                    title: '提示',
                    msg: result.message
                });

            };
            if (result.success == false) {
                $.messager.show({
                    title: '错误提示',
                    msg: result.errorMsg
                });
            }
        }
    });
}

